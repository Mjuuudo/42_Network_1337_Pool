#include <unistd.h>

void	_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if (nb < 0)
	{
		_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr((nb / 10));
	}
	_putchar((nb % 10) + '0');
}
