int	ft_is_prime(int nb)
{
	int	d;

	d = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (d <= (nb / 2))
	{
		if (nb % d == 0)
			return (0);
		d++;
	}
	return (1);
}
